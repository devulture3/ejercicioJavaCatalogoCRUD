    private void btnGuardarProdcutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProdcutoActionPerformed
        // TODO add your handling code here:
        if (!"".equals(txtCodigoProducto.getText()) && !"".equals(txtNombreProducto.getText()) && !"".equals(txtPrecioProducto.getText()) && !"".equals(cbxCatalogoProductos.getSelectedItem())) {
            pro.setCodigo(txtCodigoProducto.getText());
            pro.setNombre(txtNombreProducto.getText());
            pro.setCatalogo(cbxCatalogoProductos.getSelectedItem().toString());
            pro.setPrecio(Double.parseDouble(txtPrecioProducto.getText()));
            prod.registrarProducto(pro);
            cleanTable();
            listarProductos();
            limpiarProductos();
            JOptionPane.showMessageDialog(null, "Producto registrado exitosamente");
        } else {
            JOptionPane.showMessageDialog(null, "Algun campo esta VACIO");
        }
    }//GEN-LAST:event_btnGuardarProdcutoActionPerformed

