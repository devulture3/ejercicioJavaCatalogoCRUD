    //LISTAMOS LA TABLA DE CATEGORIA PARA QUE SE MUESTRE EN PANTALLA
    public void listarCategorias() {
        List<Catalogo> listarCat = catD.listarCategorias();
        modelo = (DefaultTableModel) tableCategoria.getModel();
        Object[] obj = new Object[2];
        for (int i = 0; i < listarCat.size(); i++) {
            obj[0] = listarCat.get(i).getId();
            obj[1] = listarCat.get(i).getNombre();
            modelo.addRow(obj);
        }
        tableCategoria.setModel(modelo);
    }
    
    //LISTAMOS LA TABLA DE PRODUCTOS PARA QUE SE MUESTRE EN PANTALLA
    public void listarProductos() {
        List<Productos> listarPro = prod.listarProductos();
//        System.out.println("listarPro = " + listarPro.get(0).getId());
        modelo1 = (DefaultTableModel) tableProductos.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < listarPro.size(); i++) {
            ob[0] = listarPro.get(i).getId();
            ob[1] = listarPro.get(i).getCodigo();
            ob[2] = listarPro.get(i).getNombre();
            ob[3] = listarPro.get(i).getCatalogo();
            ob[4] = listarPro.get(i).getPrecio();
            modelo1.addRow(ob);
        }
        tableProductos.setModel(modelo1);
    }

    public void cleanTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
        for (int i = 0; i < modelo1.getRowCount(); i++) {
            modelo1.removeRow(i);
            i = i - 1;
        }
    }

