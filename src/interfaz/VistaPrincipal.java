    private void btnGuardarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCategoriaActionPerformed
        // Boton guardar provedor
        if ("".equals(txtIDCategoria.getText())) {
            if (!"".equals(txtNombreCatalogo.getText())) {
                cat.setNombre(txtNombreCatalogo.getText());
                catD.registrarCategoria(cat);
                JOptionPane.showMessageDialog(null, "categoria registrada exitosamente");
                cleanTable();
                listarCategorias();
                limpiarCatalogo();
            } else {
                JOptionPane.showMessageDialog(null, "Campo NOMBRE vacio");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Limpie los Campos con boton NUEVO");
        }

    }//GEN-LAST:event_btnGuardarCategoriaActionPerformed

